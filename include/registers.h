#ifndef __REGISTERS_H_
#define __REGISTERS_H_

/* 
   register codes (for use as part of an opcode) are:
     0 - 12 for R0 - R12.
     14 for R14.

   13 and 15 can be used in certain instructions.
*/

typedef enum {
  r0 = 0,
  r1 = 1,
  r2 = 2,
  r3 = 3,
  r4 = 4,
  r5 = 5,
  r6 = 6,
  r7 = 7,
  r8 = 8,
  r9 = 9,
  r10 = 10,
  r11 = 11,
  r12 = 12,
  r13 = 13,
  r14 = 14,
  r15 = 15
} reg_t;

#define SP r13
#define LR r14
#define PC r15

#endif
